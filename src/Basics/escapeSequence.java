package Basics;

public class escapeSequence {
    public static void main(String[] args) {

//        \n - New line
//        \t - Horizontal Tab Space
//        \b - Back Space
//        \' - A single quote
//        \" - A double quote
//        \\ - Backslash

        System.out.println("Apple\nBanana");
        System.out.println("Apple\tBanana");
        System.out.println("Apple Banana8\b");
        System.out.println("I\'m a Programmer.");
        System.out.println("I create \"bugs\".");


    }
}
